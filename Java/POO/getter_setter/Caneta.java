package getter_setter;

public class Caneta {
    private string modelo;
    private float ponta;

    public string getModelo() {
        return modelo;
    }

    public void setModelo(string modelo) {
        this.modelo = modelo;
    }

    public float getPonta() {
        return this.ponta;
    }

    public void setPonta(float ponta) {
        this.ponta = ponta;
    }
}
